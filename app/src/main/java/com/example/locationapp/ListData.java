package com.example.locationapp;

public class ListData {
    String name, price;
    int features, desc;
    int image;

    public ListData(String name, String price, int features, int desc, int image) {
        this.name = name;
        this.price = price;
        this.features = features;
        this.desc = desc;
        this.image = image;
    }
}